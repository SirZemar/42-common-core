#include <stdio.h>

int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}

int	main()
{
	int	c = 89;
	int	result = ft_isalpha(c);
	printf("%d\n", result);
}