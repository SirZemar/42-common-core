#include <stdio.h>

int	ft_isalnum(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122) || (c >= 48 && c <= 57))
		return (1);
	return (0);
}

int	main()
{
	int	c = 70;
	int	result = ft_isalnum(c);
	printf("%d\n", result);
}
