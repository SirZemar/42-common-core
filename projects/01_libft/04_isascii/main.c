#include <stdio.h>

int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	return (0);
}

int	main()
{
	int	c = 127;
	int	result = ft_isascii(c);
	printf("%d\n", result);
}